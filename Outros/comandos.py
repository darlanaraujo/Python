# Comando para definir quantidade de caracteres em um texto digitado, e a ordem de alinhamento;
nome = input ('Olá qual o seu nome? ')
print ('Prazer em te conhecer {:=>20}'.format (nome))
#Ao colocar : e alguma informação dentro das {} ele replica a informação;
# ^ Centraliza o texto;
# > Alinha a partir da direita;
# < Alinha a partir da esquerda;
# '20' O numero é a definição da quantidade de caracteres que ele vai criar, incluindo o que for digitado;
